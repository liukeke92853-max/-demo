const pageSize = 5;
let currentPage = 1;
let scanQueue = [];
let running = false;
let stopRequested = false;
let productFilter = "";
let leaderFilter = "";

const table = document.querySelector("#requestTable");
const pageTotal = document.querySelector("#pageTotal");
const pageButtons = document.querySelector("#pageButtons");
const prevPage = document.querySelector("#prevPage");
const nextPage = document.querySelector("#nextPage");
const productSearch = document.querySelector("#productSearch");
const leaderSearch = document.querySelector("#leaderSearch");
const pluginLeader = document.querySelector("#pluginLeader");
const pluginType = document.querySelector("#pluginType");
const pluginScope = document.querySelector("#pluginScope");
const pluginLimit = document.querySelector("#pluginLimit");
const pluginDateLimit = document.querySelector("#pluginDateLimit");
const scanButton = document.querySelector("#scanButton");
const approveButton = document.querySelector("#approveButton");
const stopButton = document.querySelector("#stopButton");
const runState = document.querySelector("#runState");
const metricMatched = document.querySelector("#metricMatched");
const metricApproved = document.querySelector("#metricApproved");
const metricSkipped = document.querySelector("#metricSkipped");
const scanList = document.querySelector("#scanList");
const logList = document.querySelector("#logList");
const toast = document.querySelector("#toast");

function formatDate(date) {
  return date.replaceAll("-", "/");
}

function getFilteredRows() {
  return rows.filter((row) => {
    const productHit =
      !productFilter ||
      row.product.includes(productFilter) ||
      row.productId.includes(productFilter);
    const leaderHit =
      !leaderFilter ||
      row.leader.includes(leaderFilter) ||
      row.leaderId.includes(leaderFilter);
    return productHit && leaderHit;
  });
}

function getPageRows() {
  const filtered = getFilteredRows();
  const start = (currentPage - 1) * pageSize;
  return filtered.slice(start, start + pageSize);
}

function getTotalPages() {
  return Math.max(1, Math.ceil(getFilteredRows().length / pageSize));
}

function createIdButton(label, value) {
  return `<button class="id-chip" type="button" data-copy="${value}" title="复制 ${label}">ID</button>`;
}

function getRowStatus(row) {
  if (row.approved) return `<span class="status-chip done">已通过</span>`;
  const queued = scanQueue.some((item) => item.id === row.id);
  if (queued) return `<span class="status-chip match">可自动通过</span>`;
  return "";
}

