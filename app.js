const pageSize = 5;
let currentPage = 1;
let productFilter = "";
let leaderFilter = "";
let executedTaskCount = 24;

const table = document.querySelector("#requestTable");
const pageTotal = document.querySelector("#pageTotal");
const pageButtons = document.querySelector("#pageButtons");
const prevPage = document.querySelector("#prevPage");
const nextPage = document.querySelector("#nextPage");
const productSearch = document.querySelector("#productSearch");
const leaderSearch = document.querySelector("#leaderSearch");
const extensionLatestTime = document.querySelector("#extensionLatestTime");
const extensionTaskCount = document.querySelector("#extensionTaskCount");
const toast = document.querySelector("#toast");

function formatDate(date) {
  return date.replaceAll("-", "/");
}

function getFilteredRows() {
  return rows.filter((row) => {
    const productHit =
      !productFilter ||
      row.product.includes(productFilter) ||
      row.productId.includes(productFilter);
    const leaderHit =
      !leaderFilter ||
      row.leader.includes(leaderFilter) ||
      row.leaderId.includes(leaderFilter);
    return productHit && leaderHit;
  });
}

function getPageRows() {
  const filtered = getFilteredRows();
  const start = (currentPage - 1) * pageSize;
  return filtered.slice(start, start + pageSize);
}

function getTotalPages() {
  return Math.max(1, Math.ceil(getFilteredRows().length / pageSize));
}

function createIdButton(label, value) {
  return `<button class="id-chip" type="button" data-copy="${value}" title="复制 ${label}">ID</button>`;
}

function getRowStatus(row) {
  if (row.approved) return `<span class="status-chip done">已通过</span>`;
  return "";
}

function renderTable() {
  const pageRows = getPageRows();
  table.innerHTML = pageRows
    .map((row) => {
      return `
        <article class="table-grid table-row ${row.approved ? "is-approved" : ""}" data-row-id="${row.id}">
          <div class="product-cell">
            <img class="thumb" src="${row.image}" alt="${row.product}" />
            <div>
              <div class="title-line">
                <span class="name-ellipsis">${row.product}</span>
                ${createIdButton("商品ID", row.productId)}
              </div>
              <div class="meta">
                <div>库存：${row.stock}</div>
                <div class="price">到手价：${row.price}</div>
              </div>
            </div>
          </div>
          <div>
            <div class="title-line">
              <span>${row.leader}</span>
              ${createIdButton("团长ID", row.leaderId)}
            </div>
            <div class="meta">电话ⓘ：${row.phone} 〰</div>
          </div>
          <div class="request-cell">
            <div class="request-title">${row.requestType}</div>
            <div class="meta">
              <div>延长至${formatDate(row.extendTo)}</div>
              <div>原为${formatDate(row.originalTo)}</div>
            </div>
            ${getRowStatus(row)}
          </div>
          <div>
            <div class="activity-title">
              ${row.activity}
              ${createIdButton("活动ID", row.activityId)}
            </div>
            <div class="meta">
              <div>原佣金率：${row.commission}</div>
              <div>原服务费率：${row.serviceFee}</div>
            </div>
          </div>
          <div class="action-cell">
            <button class="link-button approve-single" type="button" data-approve="${row.id}" ${row.approved ? "disabled" : ""}>同意</button>
            <button class="link-button" type="button" ${row.approved ? "disabled" : ""}>拒绝</button>
          </div>
        </article>
      `;
    })
    .join("");

  pageTotal.textContent = `共 ${getFilteredRows().length} 条`;
  renderPagination();
}

function renderPagination() {
  const totalPages = getTotalPages();
  if (currentPage > totalPages) currentPage = totalPages;

  pageButtons.innerHTML = Array.from({ length: totalPages }, (_, index) => {
    const page = index + 1;
    return `<button class="${page === currentPage ? "is-active" : ""}" type="button" data-page="${page}">${page}</button>`;
  }).join("");

  prevPage.disabled = currentPage === 1;
  nextPage.disabled = currentPage === totalPages;
}

function showToast(message) {
  toast.textContent = message;
  toast.classList.add("show");
  window.clearTimeout(showToast.timer);
  showToast.timer = window.setTimeout(() => {
    toast.classList.remove("show");
  }, 1600);
}

async function copyText(value) {
  if (navigator.clipboard?.writeText) {
    await navigator.clipboard.writeText(value);
    return;
  }

  const helper = document.createElement("textarea");
  helper.value = value;
  helper.setAttribute("readonly", "");
  helper.style.position = "fixed";
  helper.style.top = "-999px";
  document.body.append(helper);
  helper.select();
  document.execCommand("copy");
  helper.remove();
}

function getCurrentTimeText() {
  const now = new Date();
  const date = now.toLocaleDateString("zh-CN", {
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  });
  const time = now.toLocaleTimeString("zh-CN", {
    hour: "2-digit",
    minute: "2-digit",
    hour12: false,
  });
  return `${date} ${time}`;
}

function updateExtensionStats() {
  extensionLatestTime.textContent = getCurrentTimeText();
  extensionTaskCount.textContent = String(executedTaskCount);
}

function approveRow(rowId, source = "手动") {
  const row = rows.find((item) => item.id === rowId);
  if (!row || row.approved) return false;
  row.approved = true;
  executedTaskCount += 1;
  updateExtensionStats();
  showToast(`${source}通过：${row.leader}`);
  return true;
}

document.querySelector("#pageQuery").addEventListener("click", () => {
  productFilter = productSearch.value.trim();
  leaderFilter = leaderSearch.value.trim();
  currentPage = 1;
  renderTable();
});

document.querySelector("#pageReset").addEventListener("click", () => {
  productSearch.value = "";
  leaderSearch.value = "";
  productFilter = "";
  leaderFilter = "";
  currentPage = 1;
  renderTable();
});

pageButtons.addEventListener("click", (event) => {
  const button = event.target.closest("[data-page]");
  if (!button) return;
  currentPage = Number(button.dataset.page);
  renderTable();
});

prevPage.addEventListener("click", () => {
  currentPage = Math.max(1, currentPage - 1);
  renderTable();
});

nextPage.addEventListener("click", () => {
  currentPage = Math.min(getTotalPages(), currentPage + 1);
  renderTable();
});

table.addEventListener("click", async (event) => {
  const copyButton = event.target.closest("[data-copy]");
  const approveButtonTarget = event.target.closest("[data-approve]");

  if (copyButton) {
    await copyText(copyButton.dataset.copy);
    showToast(`已复制：${copyButton.dataset.copy}`);
  }

  if (approveButtonTarget) {
    approveRow(approveButtonTarget.dataset.approve);
    renderTable();
  }
});

updateExtensionStats();
renderTable();
